-- STEP 2: CREATE TABLES

DROP TABLE IF EXISTS Owner;

CREATE TABLE Owner (
    OwnerID INT PRIMARY KEY,
    Name VARCHAR(100),
    ContactNumber VARCHAR(15),
    HouseNumber VARCHAR(10),
    Street VARCHAR(100),
    City VARCHAR(50),
    PIN VARCHAR(10)
);

DROP TABLE IF EXISTS OwnerEmail;

CREATE TABLE OwnerEmail (
    OwnerID INT,
    Email VARCHAR(100),
    PRIMARY KEY (OwnerID, Email),
    FOREIGN KEY (OwnerID) REFERENCES Owner(OwnerID)
);

DROP TABLE IF EXISTS Property;

CREATE TABLE Property (
    PropertyID INT PRIMARY KEY,
    Type VARCHAR(50),
    Status VARCHAR(20),
    Size INT,
    Price DECIMAL(10, 2),
    HouseNumber VARCHAR(10),
    Street VARCHAR(100),
    City VARCHAR(50),
    PIN VARCHAR(10),
    OwnerID INT,
    FOREIGN KEY (OwnerID) REFERENCES Owner(OwnerID)
);

DROP TABLE IF EXISTS Buyer;

CREATE TABLE Buyer (
    BuyerID INT PRIMARY KEY,
    Name VARCHAR(100),
    ContactNumber VARCHAR(15),
    HouseNumber VARCHAR(10),
    Street VARCHAR(100),
    City VARCHAR(50),
    PIN VARCHAR(10)
);

DROP TABLE IF EXISTS BuyerEmail;

CREATE TABLE BuyerEmail (
    BuyerID INT,
    Email VARCHAR(100),
    PRIMARY KEY (BuyerID, Email),
    FOREIGN KEY (BuyerID) REFERENCES Buyer(BuyerID)
);

DROP TABLE IF EXISTS Agency;

CREATE TABLE Agency (
    AgencyID INT PRIMARY KEY,
    Name VARCHAR(100),
    ContactNumber VARCHAR(15),
    HouseNumber VARCHAR(10),
    Street VARCHAR(100),
    City VARCHAR(50),
    PIN VARCHAR(10),
    LicenseNumber VARCHAR(50)
);

DROP TABLE IF EXISTS Agent;

CREATE TABLE Agent (
    AgentID INT,
    AgencyID INT,
    Name VARCHAR(100),
    ContactNumber VARCHAR(15),
    CommissionRate DECIMAL(5, 2),
    PRIMARY KEY (AgentID, AgencyID),
    FOREIGN KEY (AgencyID) REFERENCES Agency(AgencyID)
);

DROP TABLE IF EXISTS AgentEmail;

CREATE TABLE AgentEmail (
    AgentID INT,
    AgencyID INT,
    Email VARCHAR(100),
    PRIMARY KEY (AgentID, AgencyID, Email),
    FOREIGN KEY (AgentID, AgencyID) REFERENCES Agent(AgentID, AgencyID)
);

DROP TABLE IF EXISTS PropertyAgency;

CREATE TABLE PropertyAgency (
    PropertyID INT,
    AgencyID INT,
    PRIMARY KEY (PropertyID, AgencyID),
    FOREIGN KEY (PropertyID) REFERENCES Property(PropertyID),
    FOREIGN KEY (AgencyID) REFERENCES Agency(AgencyID)
);

DROP TABLE IF EXISTS BuyerAgency;

CREATE TABLE BuyerAgency (
    BuyerID INT,
    AgencyID INT,
    PRIMARY KEY (BuyerID, AgencyID),
    FOREIGN KEY (BuyerID) REFERENCES Buyer(BuyerID),
    FOREIGN KEY (AgencyID) REFERENCES Agency(AgencyID)
);

DROP TABLE IF EXISTS BuyerAgent;

CREATE TABLE BuyerAgent (
    BuyerID INT,
    AgentID INT,
    AgencyID INT,
    PRIMARY KEY (BuyerID, AgentID, AgencyID),
    FOREIGN KEY (BuyerID) REFERENCES Buyer(BuyerID),
    FOREIGN KEY (AgentID, AgencyID) REFERENCES Agent(AgentID, AgencyID)
);

DROP TABLE IF EXISTS PotentialBuyers;

CREATE TABLE PotentialBuyers (
    BuyerID INT,
    PropertyID INT,
    PRIMARY KEY (BuyerID, PropertyID),
    FOREIGN KEY (BuyerID) REFERENCES Buyer(BuyerID),
    FOREIGN KEY (PropertyID) REFERENCES Property(PropertyID)
);

DROP TABLE IF EXISTS PropertyAgent;

CREATE TABLE PropertyAgent (
    PropertyID INT,
    AgentID INT,
    AgencyID INT,
    PRIMARY KEY (PropertyID, AgentID, AgencyID),
    FOREIGN KEY (PropertyID) REFERENCES Property(PropertyID),
    FOREIGN KEY (AgentID, AgencyID) REFERENCES Agent(AgentID, AgencyID)
);

DROP TABLE IF EXISTS OwnerAgent;

CREATE TABLE OwnerAgent (
    OwnerID INT,
    AgentID INT,
    AgencyID INT,
    PRIMARY KEY (OwnerID, AgentID, AgencyID),
    FOREIGN KEY (OwnerID) REFERENCES Owner(OwnerID),
    FOREIGN KEY (AgentID, AgencyID) REFERENCES Agent(AgentID, AgencyID)
);

DROP TABLE IF EXISTS Transcation;

CREATE TABLE Transaction (
    TransactionID INT PRIMARY KEY,
    PaymentMode VARCHAR(20),
    PropertyValue DECIMAL(10, 2),
    TransactionDateTime DATETIME,
    PropertyID INT,
    BuyerID INT,
    AgentID INT,
    AgencyID INT,
    FOREIGN KEY (PropertyID) REFERENCES Property(PropertyID),
    FOREIGN KEY (BuyerID) REFERENCES Buyer(BuyerID),
    FOREIGN KEY (AgentID, AgencyID) REFERENCES Agent(AgentID, AgencyID)
);

DROP TABLE IF EXISTS Visit;

CREATE TABLE Visit (
    VisitID INT PRIMARY KEY,
    VisitDateTime DATETIME,
    Status VARCHAR(20),
    PropertyID INT,
    BuyerID INT,
    AgentID INT,
    AgencyID INT,
    FOREIGN KEY (PropertyID) REFERENCES Property(PropertyID),
    FOREIGN KEY (BuyerID) REFERENCES Buyer(BuyerID),
    FOREIGN KEY (AgentID, AgencyID) REFERENCES Agent(AgentID, AgencyID)
);

